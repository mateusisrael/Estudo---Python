# Tentando desenvolver um Jogo da forca

n1 = int(input('Digite o primeiro número: '))
n2 = int(input('Digite o segundo número: '))

while n1 != n2:
    print('\n'
          'Errou, Tente outra vez! ')
    break

else:
    print('Acertou!\n'
          '\n'
          'Fim do programa! ')
